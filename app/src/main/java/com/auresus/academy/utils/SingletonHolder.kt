package com.auresus.academy.utils
/**
 * This is generic helper class for creating the thread safe singleton with argument :
 * Use it for to create singleton as eg;  ---> companion object : SingletonHolder<LogConfigProvider, LogConfig>(::LogConfigProvider)
 */

open class SingletonHolder<out T: Any, in A>(creator: (A) -> T) {
    private var creator: ((A) -> T)? = creator
    @Volatile private var instance: T? = null

    fun getInstance(arg: A): T {
        val checkInstance = instance
        if (checkInstance != null) {
            return checkInstance
        }

        return synchronized(this) {
            val checkInstanceAgain = instance
            if (checkInstanceAgain != null) {
                checkInstanceAgain
            } else {
                val created = creator!!(arg)
                instance = created
                creator = null
                created
            }
        }
    }
}